from pydantic import BaseModel, Field


class CharterStructuredOutput(BaseModel):
    """Structured output extracted from an LLC charter analysis."""

    supreme_governing_body: str = Field(
        default="",
        description="Высший орган управления — каноническое название органа, без номера пункта",
        examples=["Общее собрание участников"],
    )
    collegial_governing_bodies: list[str] = Field(
        default_factory=list,
        description="Коллегиальные органы управления — названия органов, без номеров пунктов",
        examples=[["Совет директоров", "Наблюдательный совет", "Правление"]],
    )
    sole_executive_bodies: list[str] = Field(
        default_factory=list,
        description=(
            "Единоличные исполнительные органы — названия органов, без номеров пунктов. "
            "Только единоличные (Правление/Дирекция — коллегиальные, не включать)"
        ),
        examples=[["Генеральный директор", "Директор", "Президент", "Управляющий"]],
    )
    major_transaction_clauses: list[str] = Field(
        default_factory=list,
        description="Список пунктов о крупных сделках (каждый с указанием номера пункта устава)",
        examples=[
            [
                "10.2.15. принятие решений о согласии на совершение или о последующем "
                "одобрении крупных сделок в случаях, предусмотренных статьей 79 Федерального "
                "закона «Об акционерных обществах»",
                "10.2.16. согласие на совершение или последующее одобрение крупных сделок, "
                "предметом которых является имущество, стоимость которого составляет "
                "от 25 (двадцати пяти) до 50 (пятидесяти) процентов балансовой стоимости "
                "активов Общества, определенной по данным его бухгалтерской (финансовой) "
                "отчетности на последнюю отчетную дату",
            ]
        ],
    )
    related_party_transaction_clauses: list[str] = Field(
        default_factory=list,
        description=(
            "Список пунктов о сделках с заинтересованностью "
            "(каждый с указанием номера пункта устава)"
        ),
        examples=[
            [
                "10.5. Положения статьи 45 Закона об ООО, регулирующей порядок одобрения "
                "сделок, в совершении которых имеется заинтересованность, не применяются "
                "к Обществу."
            ]
        ],
    )
    general_meeting_minutes_protocol: str = Field(
        default="",
        description="Протокол общего собрания — номер пункта устава и способ удостоверения",
        examples=[
            "6.10. в Обществе, состоящем из одного участника, решения по вопросам, "
            "относящимся к компетенции Общего собрания участников, принимаются "
            "единственным участником единолично и оформляются письменно. Принятие Общим "
            "собранием участников решения и определение состава участников Общества, "
            "присутствовавших при его принятии, подтверждаются: - при проведении общего "
            "собрания участников в форме собрания (совместное присутствие) - подписанием "
            "протокола председателем и секретарем Общего собрания участников, являющимися "
            "участниками Общества; - при проведении общего собрания участников в форме "
            "заочного голосования (опросным путем) - подписанием протокола Генеральным "
            "директором Общества."
        ],
    )
    sole_executive_body_restrictions: list[str] = Field(
        default_factory=list,
        description=(
            "Уставные ограничения единоличного исполнительного органа "
            "(каждое с указанием номера пункта устава)"
        ),
        examples=[
            [
                "9.5. Одобрение обеспечения по обязательствам третьих лиц вне зависимости "
                "от суммы сделки"
            ]
        ],
    )
